package br.myrest.app.model;

public class Municipio {

	private String Nome;
	private int Populacao;
	
	public String getNome() {
		return Nome;
	}
	public void setNome(String nome) {
		Nome = nome;
	}
	public int getPopulacao() {
		return Populacao;
	}
	public void setPopulacao(int populacao) {
		Populacao = populacao;
	}
	
	
}
